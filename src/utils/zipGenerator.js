async function executeZIPGeneration() {
  const description = document.getElementById('zip-description').value.trim();
  const type = document.getElementById('zip-type').value;

  if (!description) {
    showToast('Please describe what to generate', 'error');
    return;
  }

  closeModal();
  hideWelcome();

  const progressId = appendProgressMessage('📦 Generating project files...');

  try {
    const typePrompts = {
      'web': 'Create a complete web app with index.html (2000+ lines), styles.css (1000+ lines), app.js (2000+ lines), README.md',
      'react': 'Create a complete React app with src/App.jsx (2000+ lines), src/components/*.jsx, src/styles/main.css, package.json, README.md',
      'python': 'Create a complete Python project with main.py (2000+ lines), utils.py (1000+ lines), requirements.txt, README.md',
      'nodejs': 'Create a complete Node.js app with src/server.js (2000+ lines), src/routes/*.js, src/models/*.js, package.json, README.md',
      'fullstack': 'Create a complete full-stack app with frontend/index.html, frontend/app.js, backend/server.js (3000+ lines), README.md, package.json',
      'custom': 'Create the requested files with complete, working code'
    };

    updateProgress(progressId, 'Generating code with AI...', 20);

    const aiPrompt = `${typePrompts[type]}

Project description: ${description}

CRITICAL REQUIREMENTS:
1. Write COMPLETE, working code — NO placeholders, NO "..." shortcuts
2. Each file must have 1000-3000+ lines of actual code
3. Include comprehensive comments
4. Add error handling throughout
5. Make it production-ready

Return the response as JSON with this structure:
{
  "projectName": "project-name",
  "files": [
    {
      "name": "filename.ext",
      "path": "relative/path/filename.ext", 
      "content": "complete file content here"
    }
  ]
}
Only return valid JSON, no markdown.`;

    const response = await getQuickResponse(aiPrompt, 'llama-3.3-70b-versatile', SYSTEM_PROMPTS.code);

    updateProgress(progressId, 'Parsing generated files...', 60);

    let projectData;
    try {
      const jsonMatch = response.match(/\{[\s\S]*\}/);
      projectData = jsonMatch ? JSON.parse(jsonMatch[0]) : null;
    } catch {
      // Fallback: create files from markdown code blocks
      projectData = extractFilesFromMarkdown(response, description, type);
    }

    if (!projectData || !projectData.files || projectData.files.length === 0) {
      // Last resort fallback
      projectData = await generateFallbackProject(description, type);
    }

    updateProgress(progressId, 'Creating ZIP archive...', 80);

    // Create ZIP
    const zip = new JSZip();
    const projectName = projectData.projectName || `mi-ai-project-${Date.now()}`;
    const folder = zip.folder(projectName);

    projectData.files.forEach(file => {
      const filePath = file.path || file.name;
      const parts = filePath.split('/');
      if (parts.length > 1) {
        let currentFolder = folder;
        for (let i = 0; i < parts.length - 1; i++) {
          currentFolder = currentFolder.folder(parts[i]);
        }
        currentFolder.file(parts[parts.length - 1], file.content);
      } else {
        folder.file(file.name, file.content);
      }
    });

    // Add MI AI README
    folder.file('MI-AI-INFO.md', `# Generated by MI AI
**Created by:** Muaaz Iqbal | Muslim Islam Org
**Date:** ${new Date().toLocaleString()}
**Project:** ${description}
**Type:** ${type}
**Files:** ${projectData.files.length}

بِسْمِ اللَّهِ الرَّحْمَٰنِ الرَّحِيمِ

This project was generated using MI AI powered by Groq.
`);

    updateProgress(progressId, 'Compressing...', 95);

    const content = await zip.generateAsync({
      type: 'blob',
      compression: 'DEFLATE',
      compressionOptions: { level: 9 }
    });

    const url = URL.createObjectURL(content);
    const a = document.createElement('a');
    a.href = url;
    a.download = `${projectName}.zip`;
    a.click();
    URL.revokeObjectURL(url);

    removeProgressMessage(progressId);
    updateProgress(progressId, 'Complete!', 100);

    const fileList = projectData.files.map(f => `• ${f.path || f.name}`).join('\n');
    appendMessage('assistant', `✅ **ZIP Generated: ${projectName}.zip**\n\n**Files created:**\n${fileList}\n\nDownloaded to your device!`, true);
    showToast('ZIP downloaded!', 'success');

  } catch (err) {
    removeProgressMessage(progressId);
    appendMessage('assistant', `❌ ZIP generation failed: ${err.message}`, true);
  }
}

// ---- Extract files from markdown ----
function extractFilesFromMarkdown(text, description, type) {
  const files = [];
  const codeBlockRegex = /```(\w+)?\s*\n([\s\S]*?)```/g;
  const filenameRegex = /(?:File:|Filename:|###)\s*([^\n]+\.[a-z]+)/gi;

  let match;
  let fileIndex = 0;
  const extensions = { web: ['html', 'css', 'js'], react: ['jsx', 'css', 'json'], python: ['py', 'txt'], nodejs: ['js', 'json'] };

  while ((match = codeBlockRegex.exec(text)) !== null) {
    const lang = match[1] || 'txt';
    const content = match[2];
    const ext = { javascript: 'js', python: 'py', typescript: 'ts', html: 'html', css: 'css', json: 'json', bash: 'sh' }[lang] || lang;

    files.push({
      name: `file_${++fileIndex}.${ext}`,
      path: `file_${fileIndex}.${ext}`,
      content
    });
  }

  return {
    projectName: `mi-ai-${type}-${Date.now()}`,
    files: files.length > 0 ? files : [{ name: 'README.md', path: 'README.md', content: text }]
  };
}

// ---- Fallback project ----
