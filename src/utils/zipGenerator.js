let voiceRecognition = null;
let isVoiceActive = false;
let speechSynthesis = window.speechSynthesis;
let currentUtterance = null;

function toggleVoice() {
  if (isVoiceActive) {
    stopVoice();
  } else {
    startVoice();
  }
}

function startVoice() {
  if (!('webkitSpeechRecognition' in window) && !('SpeechRecognition' in window)) {
    showToast('Voice recognition not supported in this browser', 'error');
    return;
  }

  const SpeechRecognition = window.SpeechRecognition || window.webkitSpeechRecognition;
  voiceRecognition = new SpeechRecognition();
  voiceRecognition.continuous = false;
  voiceRecognition.interimResults = true;
  voiceRecognition.lang = 'en-US';

  voiceRecognition.onstart = () => {
    isVoiceActive = true;
    document.getElementById('voice-btn').classList.add('active');
    showVoiceIndicator();
    showToast('🎤 Listening...', 'info');
  };

  voiceRecognition.onresult = (event) => {
    let transcript = '';
    for (let i = event.resultIndex; i < event.results.length; i++) {
      transcript += event.results[i][0].transcript;
    }
    document.getElementById('message-input').value = transcript;
    autoResize(document.getElementById('message-input'));
  };

  voiceRecognition.onend = () => {
    isVoiceActive = false;
    document.getElementById('voice-btn').classList.remove('active');
    hideVoiceIndicator();

    const input = document.getElementById('message-input').value.trim();
    if (input) {
      setTimeout(() => sendMessage(), 500);
    }
  };

  voiceRecognition.onerror = (event) => {
    isVoiceActive = false;
    document.getElementById('voice-btn').classList.remove('active');
    hideVoiceIndicator();
    if (event.error !== 'aborted') {
      showToast('Voice error: ' + event.error, 'error');
    }
  };

  voiceRecognition.start();
}

function stopVoice() {
  if (voiceRecognition) {
    voiceRecognition.stop();
  }
  isVoiceActive = false;
  document.getElementById('voice-btn').classList.remove('active');
  hideVoiceIndicator();
}

function showVoiceIndicator() {
  let indicator = document.getElementById('voice-indicator');
  if (!indicator) {
    indicator = document.createElement('div');
    indicator.id = 'voice-indicator';
    indicator.className = 'voice-indicator';
    indicator.innerHTML = '<div class="voice-pulse"></div><span>🎤 Listening... Speak now</span><button onclick="stopVoice()" style="background:none;border:none;color:var(--accent);cursor:pointer;margin-left:8px;">Stop</button>';
    document.querySelector('.input-area').insertBefore(indicator, document.querySelector('.input-wrapper'));
  }
}

function hideVoiceIndicator() {
  const indicator = document.getElementById('voice-indicator');
  if (indicator) indicator.remove();
}

// Text to Speech
function speakText(text) {
  if (!speechSynthesis) return;

  // Stop current speech
  speechSynthesis.cancel();

  const cleanText = text.replace(/[#*`_\[\]()]/g, '').replace(/\n+/g, ' ').substring(0, 2000);
  currentUtterance = new SpeechSynthesisUtterance(cleanText);
  currentUtterance.rate = 0.9;
  currentUtterance.pitch = 1;
  currentUtterance.volume = 1;

  const voices = speechSynthesis.getVoices();
  const preferredVoice = voices.find(v => v.lang === 'en-US' && v.name.includes('Google')) || 
                          voices.find(v => v.lang === 'en-US') ||
                          voices[0];
  if (preferredVoice) currentUtterance.voice = preferredVoice;

  speechSynthesis.speak(currentUtterance);
}

function stopSpeaking() {
  if (speechSynthesis) speechSynthesis.cancel();
}

