let userBehaviorData = {
  queries: [],
  preferences: {},
  sessionStart: Date.now(),
  totalMessages: 0,
  preferredModes: {},
  preferredModels: {}
};

function collectUserData(user) {
  if (!user) return;

  // Load existing data
  db.ref(`users/${user.uid}/behavior`).once('value').then(snap => {
    if (snap.exists()) {
      userBehaviorData = { ...userBehaviorData, ...snap.val() };
    }
  });
}

function trackInteraction(type, data) {
  userBehaviorData.totalMessages++;

  if (type === 'mode') {
    userBehaviorData.preferredModes[data] = (userBehaviorData.preferredModes[data] || 0) + 1;
  }
  if (type === 'model') {
    userBehaviorData.preferredModels[data] = (userBehaviorData.preferredModels[data] || 0) + 1;
  }
  if (type === 'query' && data) {
    userBehaviorData.queries.push({
      text: data.substring(0, 100),
      timestamp: Date.now()
    });
    // Keep only last 50 queries
    if (userBehaviorData.queries.length > 50) {
      userBehaviorData.queries = userBehaviorData.queries.slice(-50);
    }
  }

  // Save to Firebase every 5 messages
  if (userBehaviorData.totalMessages % 5 === 0 && currentUser) {
    db.ref(`users/${currentUser.uid}/behavior`).set(userBehaviorData).catch(() => {});
  }
}

// Get personalized suggestions based on behavior
function getPersonalizedSuggestions() {
  const topMode = Object.entries(userBehaviorData.preferredModes)
    .sort(([,a],[,b]) => b - a)[0]?.[0] || 'chat';

  const suggestions = {
    code: ['Debug my Python code', 'Write a REST API in Node.js', 'Create a React component'],
    pdf: ['Generate a book on Machine Learning', 'Create a research paper', 'Write an Islamic book'],
    quran: ['Explain Surah Al-Baqarah', 'Tell me about prayer times', 'Hadith on knowledge'],
    image: ['Generate Islamic geometric art', 'Create a nature landscape', 'Abstract digital art'],
    chat: ['Explain quantum computing', 'Help me plan a project', 'Translate to Arabic']
  };

  return suggestions[topMode] || suggestions.chat;
}
