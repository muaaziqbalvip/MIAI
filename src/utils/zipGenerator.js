    history1.push({ role: 'assistant', content: alphaResponse });
    removeThinkingMessage(alpha_thinking);

    // Display dual panels
    const dualHtml = `
      <div class="dual-ai-container">
        <div class="dual-ai-panel">
          <div class="dual-ai-header ai-alpha-header">
            🤖 AI Alpha — Round ${round}
            <small style="margin-left:auto; font-weight:400">${GROQ_MODELS[model1]?.name || model1}</small>
          </div>
          <div class="dual-ai-body">${renderMarkdownSimple(alphaResponse)}</div>
        </div>
      </div>
    `;
    appendMessage('assistant', '', true, dualHtml);

    // AI Beta responds
    const beta_thinking = appendThinkingMessage(`🤖 AI Beta (Round ${round}) is thinking...`);

    const betaPrompt = `You are AI Beta debating with AI Alpha about: "${topic}". 
AI Alpha just said: "${alphaResponse.substring(0, 500)}..."
Present a different perspective, challenge AI Alpha's arguments, and provide your own analysis.
Round: ${round} of ${rounds}`;

    const betaResponse = await getQuickResponse(betaPrompt, model2,
      'You are a brilliant debater. Challenge arguments intelligently and present unique perspectives.');

    history2.push({ role: 'assistant', content: betaResponse });
    removeThinkingMessage(beta_thinking);

